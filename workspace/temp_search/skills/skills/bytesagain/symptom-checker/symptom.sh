set -euo pipefail

CMD="${1:-help}"
shift 2>/dev/null || true

TRACKER_FILE="/tmp/symptom_tracker.txt"

show_help() {
  cat <<'EOF'
╔══════════════════════════════════════════════════════════╗
║            🏥 Symptom Checker 症状自查工具               ║
╚══════════════════════════════════════════════════════════╝

⚠️ 免责声明: 本工具仅供健康知识参考，不能替代专业医疗诊断。
   如有不适，请及时就医。紧急情况请拨打120。

Usage: bash symptom.sh <command> [args...]

Commands:
  check      <symptom> [duration] [severity]   症状分析
      severity: mild | moderate | severe
  when-to-see <symptom>                        是否需要就医
  prevention <condition>                       预防建议
  lifestyle  <concern>                         生活方式改善
  tracker    <symptom> <date> [notes]           记录症状
  emergency  <symptom>                         急症识别
  help                                          显示此帮助

Examples:
  bash symptom.sh check headache 3days moderate
  bash symptom.sh when-to-see chest-pain
  bash symptom.sh prevention cold
  bash symptom.sh lifestyle sedentary
  bash symptom.sh tracker headache 2024-01-15 "午后发作"
  bash symptom.sh emergency breathing-difficulty

  Powered by BytesAgain | bytesagain.com | hello@bytesagain.com
EOF
}

disclaimer() {
  echo ""
  echo "⚠️  免责声明: 以上信息仅供参考，不构成医疗诊断或治疗建议。"
  echo "   如有不适，请及时到正规医院就诊。紧急情况请拨打120。"
}

cmd_check() {
  local symptom="${1:?用法: check <symptom> [duration] [severity]}"
  local duration="${2:-unknown}"
  local severity="${3:-moderate}"

  local severity_cn severity_icon
  case "$severity" in
    mild)     severity_cn="轻度" severity_icon="🟢" ;;
    moderate) severity_cn="中度" severity_icon="🟡" ;;
    severe)   severity_cn="重度" severity_icon="🔴" ;;
    *)        severity_cn="$severity" severity_icon="🟡" ;;
  esac

  echo "╔══════════════════════════════════════════════════════════╗"
  echo "║           🔍 症状分析: ${symptom}"
  echo "╚══════════════════════════════════════════════════════════╝"
  echo ""
  echo "📋 症状信息"
  echo "────────────────────────────────────────"
  echo "  症状:     ${symptom}"
  echo "  持续时间: ${duration}"
  echo "  严重程度: ${severity_icon} ${severity_cn}"
  echo ""

  case "$symptom" in
    headache|头痛)
      cat <<'EOF'
📊 头痛分析
────────────────────────────────────────
  常见类型:
    🔹 紧张性头痛 — 压迫感，双侧，持续数小时
    🔹 偏头痛 — 搏动性，单侧，可伴恶心
    🔹 丛集性头痛 — 剧烈，眼周，短时间反复
    🔹 颈源性头痛 — 从后脑到前额，久坐相关

  可能诱因:
    • 睡眠不足或过多
    • 压力/焦虑
    • 脱水
    • 用眼过度/屏幕时间过长
    • 颈椎问题
    • 天气变化

  初步建议:
    ✅ 充足休息，保证睡眠
    ✅ 多喝水 (每天2000ml+)
    ✅ 减少屏幕使用时间
    ✅ 适当按摩太阳穴和后颈
    ✅ 轻度可服用对乙酰氨基酚
EOF
      ;;
    stomachache|stomach-pain|胃痛|腹痛)
      cat <<'EOF'
📊 腹痛/胃痛分析
────────────────────────────────────────
  常见类型:
    🔹 上腹痛 — 胃炎、溃疡、消化不良
    🔹 下腹痛 — 肠炎、便秘、经期痛
    🔹 右上腹 — 胆囊问题
    🔹 右下腹 — 注意排除阑尾炎

  可能诱因:
    • 饮食不规律
    • 辛辣/油腻/生冷食物
    • 压力/焦虑
    • 食物不洁

  初步建议:
    ✅ 清淡饮食，少食多餐
    ✅ 避免辛辣刺激
    ✅ 腹部热敷
    ✅ 注意观察是否加重
EOF
      ;;
    fever|发烧|发热)
      cat <<'EOF'
📊 发烧分析
────────────────────────────────────────
  体温分级:
    🟢 低热: 37.3-38°C
    🟡 中热: 38.1-39°C
    🔴 高热: 39.1-41°C
    🔴 超高热: >41°C (危险!)

  常见原因:
    • 感冒/流感
    • 细菌感染
    • 炎症反应
    • 疫苗接种后

  初步建议:
    ✅ 多喝温水，补充电解质
    ✅ 物理降温 (温水擦浴)
    ✅ ≥38.5°C 可服退烧药
    ✅ 注意休息
    🔴 >39°C持续不退请就医
    🔴 儿童发烧要更加警惕
EOF
      ;;
    cough|咳嗽)
      cat <<'EOF'
📊 咳嗽分析
────────────────────────────────────────
  类型判断:
    🔹 干咳 — 无痰，可能是过敏/咽炎/气道反应
    🔹 湿咳 — 有痰，可能是感冒/支气管炎
    🔹 夜间咳 — 可能是鼻涕倒流/哮喘/胃酸反流

  持续时间判断:
    • <3周: 急性咳嗽 (感冒常见)
    • 3-8周: 亚急性 (感冒后咳嗽)
    • >8周: 慢性咳嗽 (需就医检查)

  初步建议:
    ✅ 多喝温水，蜂蜜水可缓解
    ✅ 保持空气湿润
    ✅ 避免烟雾刺激
    ✅ 干咳可含润喉片
EOF
      ;;
    *)
      cat <<EOF
📊 ${symptom} 分析
────────────────────────────────────────
  暂无此症状的详细分析数据。

  通用建议:
    ✅ 注意休息，保证充足睡眠
    ✅ 均衡饮食，多喝水
    ✅ 记录症状变化（使用 tracker 命令）
    ✅ 如症状持续或加重，请及时就医

  💡 使用 'when-to-see ${symptom}' 查看是否需要就医
EOF
      ;;
  esac

  disclaimer
}

cmd_when_to_see() {
  local symptom="${1:?用法: when-to-see <symptom>}"

  cat <<EOF
╔══════════════════════════════════════════════════════════╗
║           🏥 就医建议: ${symptom}
╚══════════════════════════════════════════════════════════╝

🔴 立即急诊 (拨打120)
────────────────────────────────────────
  • 胸痛/胸闷（尤其左侧）
  • 突然剧烈头痛（从未有过的程度）
  • 呼吸困难/气短
  • 意识模糊/晕厥
  • 大量出血无法止住
  • 严重过敏反应
  • 突然肢体无力/言语不清（中风征兆）

🟡 尽快就医 (24小时内)
────────────────────────────────────────
  • 高烧 >39°C 持续不退
  • 剧烈腹痛
  • 持续呕吐/无法进食
  • 外伤后肿胀明显
  • 小便异常（血尿/尿痛）
  • 视力突然变化

🟢 择期就医 (1-2周内)
────────────────────────────────────────
  • 症状持续超过2周未好转
  • 反复出现的症状
  • 体重无故明显变化
  • 持续疲劳/无力
  • 皮肤异常变化
  • 消化问题持续存在

💡 通用判断标准
────────────────────────────────────────
  ⚡ 突然发生 + 剧烈 → 急诊
  📈 逐渐加重 + 持续 → 尽快就医
  🔄 反复发作 + 影响生活 → 择期就医
  📉 轻微 + 好转趋势 → 观察+自我护理
EOF

  disclaimer
}

cmd_prevention() {
  local condition="${1:?用法: prevention <condition>}"

  echo "╔══════════════════════════════════════════════════════════╗"
  echo "║           🛡️ 预防建议: ${condition}"
  echo "╚══════════════════════════════════════════════════════════╝"
  echo ""

  case "$condition" in
    cold|感冒)
      cat <<'EOF'
🛡️ 感冒预防
────────────────────────────────────────
  日常防护:
    ✅ 勤洗手 (每次20秒以上)
    ✅ 保持通风 (每天开窗30分钟)
    ✅ 充足睡眠 (7-9小时)
    ✅ 均衡饮食，补充维C
    ✅ 适量运动，增强免疫力
    ✅ 人多场所戴口罩

  季节性建议:
    🍂 秋冬: 注意保暖，温差大时加衣
    🌸 春季: 注意花粉过敏区分
    ☀️ 夏季: 空调温度不要太低

  增强免疫力:
    • 每周运动150分钟+
    • 补充锌、维生素D
    • 减少压力
    • 不吸烟、限制酒精
EOF
      ;;
    diabetes|糖尿病)
      cat <<'EOF'
🛡️ 糖尿病预防
────────────────────────────────────────
  饮食管理:
    ✅ 控制碳水化合物摄入
    ✅ 多吃蔬菜和全谷物
    ✅ 减少含糖饮料和甜食
    ✅ 少食多餐

  运动建议:
    ✅ 每周150分钟中等强度运动
    ✅ 餐后散步30分钟
    ✅ 控制体重在正常范围

  定期检查:
    ✅ 每年测空腹血糖
    ✅ 有家族史者更需定期筛查
    ✅ 关注糖化血红蛋白(HbA1c)
EOF
      ;;
    *)
      cat <<EOF
🛡️ ${condition} 预防 — 通用建议
────────────────────────────────────────
  🏃 规律运动    每周150分钟中等强度
  🥗 均衡饮食    多蔬果，少油盐糖
  😴 充足睡眠    每晚7-9小时
  💧 多喝水      每天2000ml+
  🧘 压力管理    冥想、运动、社交
  🏥 定期体检    每年至少一次

  💡 提示: 支持的具体病症关键词:
     cold(感冒), diabetes(糖尿病)
     其他症状持续更新中
EOF
      ;;
  esac

  disclaimer
}

cmd_lifestyle() {
  local concern="${1:?用法: lifestyle <concern>}"

  echo "╔══════════════════════════════════════════════════════════╗"
  echo "║           🌿 生活方式改善: ${concern}"
  echo "╚══════════════════════════════════════════════════════════╝"
  echo ""

  case "$concern" in
    sedentary|久坐)
      cat <<'EOF'
🪑 久坐改善方案
────────────────────────────────────────
  每小时行动:
    ⏰ 设置每50分钟站起来活动5-10分钟
    🚶 走动、伸展、接水
    🧘 简单拉伸动作

  工位优化:
    💻 屏幕与眼睛平齐
    🪑 椅子支撑腰部曲线
    ⌨️ 手肘呈90度
    👣 脚平放地面

  运动补偿:
    🏃 每天至少30分钟运动
    🚴 通勤骑车或步行
    🧗 楼梯代替电梯
    💪 办公室简单拉伸操

  健康风险提醒:
    ⚠️ 久坐增加心血管疾病风险
    ⚠️ 可能导致腰椎/颈椎问题
    ⚠️ 影响血液循环和代谢
EOF
      ;;
    insomnia|失眠)
      cat <<'EOF'
😴 失眠改善方案
────────────────────────────────────────
  睡前习惯:
    📵 睡前1小时放下手机
    🛀 热水澡/泡脚
    📖 轻松阅读（纸质书）
    🎵 轻音乐/白噪音

  环境优化:
    🌙 遮光窗帘
    🌡️ 温度18-22°C
    🤫 安静环境/耳塞
    🛏️ 舒适床垫和枕头

  日间习惯:
    ☕ 下午2点后不喝咖啡
    🏃 规律运动(但睡前2h避免)
    ☀️ 白天多晒太阳
    😴 午睡不超过20分钟

  认知调整:
    🧘 不要在床上做睡觉以外的事
    ⏰ 睡不着就起来，有困意再上床
    📝 睡前写下担忧，明天再处理
EOF
      ;;
    *)
      cat <<EOF
🌿 ${concern} 改善建议
────────────────────────────────────────
  基础改善:
    ✅ 规律作息
    ✅ 均衡饮食
    ✅ 适量运动
    ✅ 充足睡眠
    ✅ 压力管理
    ✅ 社交活动

  💡 支持的关键词:
     sedentary(久坐), insomnia(失眠)
     其他场景持续更新中
EOF
      ;;
  esac
}

cmd_tracker() {
  local symptom="${1:?用法: tracker <symptom> <date> [notes]}"
  local date="${2:?请提供日期}"
  local notes="${3:-}"

  echo "${date} | ${symptom} | ${notes}" >> "$TRACKER_FILE"

  echo "╔══════════════════════════════════════════════════════════╗"
  echo "║           📝 症状记录"
  echo "╚══════════════════════════════════════════════════════════╝"
  echo ""
  echo "  ✅ 已记录: ${date} — ${symptom} ${notes:+(${notes})}"
  echo ""
  echo "📋 最近记录:"
  echo "────────────────────────────────────────"

  if [ -f "$TRACKER_FILE" ]; then
    tail -10 "$TRACKER_FILE" | while IFS='|' read -r d s n; do
      echo "  ${d} —${s}${n:+ (${n})}"
    done
  fi

  echo ""
  echo "💡 提示: 就医时可将此记录提供给医生参考"
  echo "   文件位置: ${TRACKER_FILE}"
}

cmd_emergency() {
  local symptom="${1:?用法: emergency <symptom>}"

  cat <<EOF
╔══════════════════════════════════════════════════════════╗
║           🚨 急症识别: ${symptom}
╚══════════════════════════════════════════════════════════╝

🆘 紧急情况请立即拨打 120 ！

EOF

  case "$symptom" in
    chest-pain|胸痛)
      cat <<'EOF'
🔴 胸痛急症识别
────────────────────────────────────────
  危险信号（可能心梗）:
    🔴 压榨性胸痛，像石头压胸口
    🔴 疼痛放射到左臂/下巴/后背
    🔴 伴出汗、恶心、呼吸困难
    🔴 持续 > 15分钟不缓解

  立即行动:
    1. 🆘 拨打120
    2. 💊 嚼服阿司匹林 (如不过敏)
    3. 🛋️ 坐下或半卧位休息
    4. 🚫 不要独自开车去医院
    5. ⏰ 记录发作时间
EOF
      ;;
    breathing-difficulty|呼吸困难)
      cat <<'EOF'
🔴 呼吸困难急症识别
────────────────────────────────────────
  危险信号:
    🔴 突然发生的严重呼吸困难
    🔴 嘴唇/指甲发紫
    🔴 无法说完整句话
    🔴 呼吸时肋间明显凹陷

  立即行动:
    1. 🆘 拨打120
    2. 🪟 保持空气流通
    3. 🧎 坐直或前倾体位
    4. 👔 松开紧身衣物
    5. 💨 如有哮喘药物，立即使用
EOF
      ;;
    stroke|中风)
      cat <<'EOF'
🔴 中风急症识别 — FAST口诀
────────────────────────────────────────
  🅵 Face(脸):   微笑时面部是否不对称?
  🅰 Arm(手臂):  双臂平举是否一侧下垂?
  🅢 Speech(言语): 说话是否含糊不清?
  🅣 Time(时间):  发现以上任一症状，立即拨打120!

  ⏰ 黄金急救时间: 4.5小时内
  每延迟1分钟，约190万脑细胞死亡

  立即行动:
    1. 🆘 拨打120，说明怀疑中风
    2. 📝 记录发病时间
    3. 🛋️ 让患者平躺，头偏向一侧
    4. 🚫 不要喂水喂药
    5. 🚫 不要等着看是否好转
EOF
      ;;
    *)
      cat <<EOF
🔴 ${symptom} 紧急处理
────────────────────────────────────────
  通用紧急处理步骤:
    1. 🆘 拨打120急救电话
    2. 📍 告知准确位置
    3. 🗣️ 描述症状和发生时间
    4. 🧘 保持冷静，等待救援
    5. 📝 记录症状变化

  🔴 需要立即急诊的情况:
    • 意识丧失
    • 大量出血
    • 严重呼吸困难
    • 剧烈胸痛
    • 严重过敏反应
    • 中毒
    • 严重外伤
EOF
      ;;
  esac

  disclaimer
}

case "$CMD" in
  check)       cmd_check "$@" ;;
  when-to-see) cmd_when_to_see "$@" ;;
  prevention)  cmd_prevention "$@" ;;
  lifestyle)   cmd_lifestyle "$@" ;;
  tracker)     cmd_tracker "$@" ;;
  emergency)   cmd_emergency "$@" ;;
  help)        show_help ;;
  *)
    echo "❌ 未知命令: $CMD"
    echo "运行 'bash symptom.sh help' 查看帮助"
    exit 1
    ;;
esac

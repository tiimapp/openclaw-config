#!/usr/bin/env bash
set -euo pipefail

# Character Creator — 角色设计工具
# Usage: bash scripts/character.sh <command> [args...]

CMD="${1:-help}"
shift 2>/dev/null || true

show_help() {
  cat <<'EOF'
Character Creator — 角色设计工具

Commands:
  create <name> <role> [world]               创建角色档案
  backstory <name> <keywords>                生成背景故事
  personality <name> <mbti> [traits]         性格设计
  relationship <name> <relations>            人物关系网
  arc <name> <arc_type> [structure]          角色弧光
  sheet <name> [format]                      完整角色卡
  help                                      显示帮助

Examples:
  character.sh create "林夜" "protagonist" "武侠"
  character.sh backstory "林夜" "orphan,martial-arts,revenge"
  character.sh personality "林夜" "INTJ" "沉稳,机敏,孤傲"
  character.sh relationship "林夜" "师父:慕容白,宿敌:萧无痕,挚友:陈小七"
  character.sh arc "林夜" "复仇者→守护者" "3acts"
  character.sh sheet "林夜" "rpg"

  Powered by BytesAgain | bytesagain.com | hello@bytesagain.com
EOF
}

cmd_create() {
  local name="${1:?请提供角色名}"
  local role="${2:?请指定角色类型 (protagonist|antagonist|mentor|sidekick|love-interest)}"
  local world="${3:-原创世界}"

  cat <<EOF
## 🧑 角色档案: ${name}

\`\`\`
╔══════════════════════════════════════════╗
║  角色档案                                 ║
╠══════════════════════════════════════════╣
║                                          ║
║  姓名:   ${name}
║  角色:   $(case "$role" in
    protagonist) echo "主角 (Protagonist)";;
    antagonist) echo "反派 (Antagonist)";;
    mentor) echo "导师 (Mentor)";;
    sidekick) echo "伙伴 (Sidekick)";;
    love-interest) echo "恋人 (Love Interest)";;
    *) echo "$role";;
  esac)
║  世界观: ${world}
║  创建:   $(date '+%Y-%m-%d')
║                                          ║
╚══════════════════════════════════════════╝
\`\`\`

### 📋 基础信息 (请补充)

| 项目 | 内容 |
|------|------|
| 全名 | ${name} |
| 年龄 | (待定) |
| 性别 | (待定) |
| 外貌 | (身高、体型、发色、特征) |
| 职业 | (待定) |
| 出生地 | (待定) |
| 现居地 | (待定) |
| 口头禅 | "(待定)" |

### 🎭 角色定位: $(case "$role" in
    protagonist) echo "主角";;
    antagonist) echo "反派";;
    mentor) echo "导师";;
    sidekick) echo "伙伴";;
    love-interest) echo "恋人";;
    *) echo "$role";;
  esac)

EOF

  case "$role" in
    protagonist)
      cat <<'EOF'
**主角设计要点**:
- 有清晰的外在目标和内在需求
- 内在矛盾让角色有深度
- 主动做出选择推动剧情
- 能力和缺陷都要突出
- 在故事中发生真实的变化
EOF
      ;;
    antagonist)
      cat <<'EOF'
**反派设计要点**:
- 有合理的动机（自认为是对的）
- 实力要足够威胁主角
- 与主角形成镜像关系
- 有令人同情或理解的一面
- 比"纯粹的邪恶"更有深度
EOF
      ;;
    mentor)
      cat <<'EOF'
**导师设计要点**:
- 有自己的过去和遗憾
- 教导方式独特
- 不能永远保护主角
- 可能在关键时刻离去
- 代表主角可能成为的样子
EOF
      ;;
    sidekick)
      cat <<'EOF'
**伙伴设计要点**:
- 与主角性格互补
- 有独立的目标和故事线
- 在关键时刻提供支持
- 不只是"工具人"
- 有自己的成长弧光
EOF
      ;;
    love-interest)
      cat <<'EOF'
**恋人设计要点**:
- 是独立完整的人物
- 与主角有化学反应
- 关系发展要有阻碍
- 改变主角的方式
- 不只存在于感情线中
EOF
      ;;
  esac

  echo ""
  echo "💡 下一步: 用 \`character.sh backstory\`、\`personality\`、\`relationship\` 深化角色"
}

cmd_backstory() {
  local name="${1:?请提供角色名}"
  local keywords="${2:?请提供关键词 (逗号分隔)}"

  IFS=',' read -ra kws <<< "$keywords"

  cat <<EOF
## 📜 背景故事: ${name}

### 关键词: ${keywords}

---

### 🧒 童年 (0-12岁)

**成长环境**: (家庭状况、社会环境)
**关键事件**: $(for kw in "${kws[@]}"; do echo "- ${kw}相关事件"; done)
**影响**: 塑造了什么样的基础性格？

### 🧑 少年 (13-18岁)

**转折点**: (改变人生轨迹的事件)
**成长**: 学到了什么？失去了什么？
**选择**: 做出了什么关键决定？

### 🧔 成年/故事开始前

**现状**: 故事开始时的状态
**日常**: 过着怎样的生活？
**隐痛**: 未解决的过去

---

### 📊 背景要素分析

| 要素 | 内容 | 对性格的影响 |
|------|------|------------|
EOF

  for kw in "${kws[@]}"; do
    kw=$(echo "$kw" | xargs)
    printf "| %s | (具体经历) | (塑造了什么特质) |\n" "$kw"
  done

  cat <<'EOF'

### 💡 背景故事检查

- [ ] 背景解释了角色的核心动机
- [ ] 背景解释了角色的主要缺陷
- [ ] 有具体的关键事件（不是泛泛描述）
- [ ] 背景与世界观一致
- [ ] 留有可以在故事中揭示的秘密
- [ ] 悲惨但不过度（避免"苦难竞赛"）

### 🎯 核心创伤

> "${name}" 最深的伤痛是什么？
> 这个创伤如何影响TA的行为模式？
> 在故事中如何面对和克服？
EOF
}

cmd_personality() {
  local name="${1:?请提供角色名}"
  local mbti="${2:?请提供MBTI类型 (如INTJ)}"
  local traits="${3:-}"

  cat <<EOF
## 🧠 性格画像: ${name}

### MBTI: ${mbti}

\`\`\`
EOF

  case "${mbti:0:1}" in
    I) echo "  能量来源: 内向 (Introversion) — 从独处中获得能量" ;;
    E) echo "  能量来源: 外向 (Extraversion) — 从社交中获得能量" ;;
  esac
  case "${mbti:1:1}" in
    N) echo "  信息收集: 直觉 (iNtuition) — 关注可能性和大局" ;;
    S) echo "  信息收集: 感觉 (Sensing) — 关注事实和细节" ;;
  esac
  case "${mbti:2:1}" in
    T) echo "  决策方式: 思考 (Thinking) — 逻辑分析" ;;
    F) echo "  决策方式: 情感 (Feeling) — 价值和感受" ;;
  esac
  case "${mbti:3:1}" in
    J) echo "  生活方式: 判断 (Judging) — 有计划，喜欢确定" ;;
    P) echo "  生活方式: 感知 (Perceiving) — 灵活，保持开放" ;;
  esac

  echo '```'
  echo ""

  if [[ -n "$traits" ]]; then
    IFS=',' read -ra trait_list <<< "$traits"
    echo "### 🎭 性格特征"
    echo ""
    echo "| 特征 | 表现 | 极端情况 |"
    echo "|------|------|---------|"
    for t in "${trait_list[@]}"; do
      t=$(echo "$t" | xargs)
      echo "| ${t} | (日常表现) | (压力下的极端表现) |"
    done
    echo ""
  fi

  cat <<EOF
### ⚖️ 性格矛盾

好的角色有内在矛盾，让他们更真实:

\`\`\`
  ${name} 的内在矛盾:

  想要 ←─────────→ 需要
  (外在目标)       (真正需要的)

  优点 ←─────────→ 缺点
  (表面展示)       (深层缺陷)

  信念 ←─────────→ 恐惧
  (坚信的事)       (最怕的事)
\`\`\`

### 😤 压力反应

| 压力等级 | 表现 |
|---------|------|
| 低压力 | 正常状态，展示优点 |
| 中等压力 | 缺陷开始显现 |
| 高压力 | 做出非理性决定 |
| 极端压力 | 突破或崩溃 |

### 🗣️ 说话方式

- 语速: (快/慢/时快时慢)
- 用词: (文雅/粗犷/精确/模糊)
- 习惯: (口头禅、停顿、手势)
- 幽默感: (干幽默/无厘头/讽刺/不善幽默)

### 🎯 核心总结

| 项目 | 内容 |
|------|------|
| 最大的优点 | (让人喜欢TA的原因) |
| 最大的缺点 | (让TA陷入麻烦的原因) |
| 最大的恐惧 | (TA最害怕什么) |
| 最大的愿望 | (TA最想得到什么) |
| 核心信念 | "（TA相信什么）" |
| 谎言 | "（TA相信但其实错了的事）" |
EOF
}

cmd_relationship() {
  local name="${1:?请提供角色名}"
  local relations="${2:?请提供关系 (关系:名字,关系:名字,...)}"

  echo "## 🔗 人物关系网: ${name}"
  echo ""

  IFS=',' read -ra rels <<< "$relations"

  echo '```'
  echo "              人物关系图"
  echo ""
  echo "                 ┌────────┐"
  echo "                 │ ${name} │"
  echo "                 └───┬────┘"

  for rel in "${rels[@]}"; do
    local rel_type="${rel%%:*}"
    local rel_name="${rel##*:}"
    printf "                     ├── %s: %s\n" "$rel_type" "$rel_name"
  done

  echo '```'
  echo ""

  echo "### 关系详情"
  echo ""
  echo "| 角色 | 关系 | 冲突 | 变化 |"
  echo "|------|------|------|------|"

  for rel in "${rels[@]}"; do
    local rel_type="${rel%%:*}"
    local rel_name="${rel##*:}"
    echo "| ${rel_name} | ${rel_type} | (关系中的矛盾) | (关系如何变化) |"
  done

  cat <<'EOF'

### 💡 关系设计原则

1. **关系创造冲突** — 最好的冲突来自亲近的人
2. **动态变化** — 关系应该随故事发展而改变
3. **双向影响** — 每个关系都改变双方
4. **多重关系** — 同一人可以是盟友又是竞争者
5. **镜像映照** — 对手往往映照主角未走的路

### 🔄 关系弧光

```
开始 ──→ 冲突 ──→ 理解 ──→ 变化
(初始状态)  (矛盾激化)  (看到对方视角)  (关系转变)
```
EOF
}

cmd_arc() {
  local name="${1:?请提供角色名}"
  local arc_type="${2:?请描述弧光 (起点→终点)}"
  local structure="${3:-3acts}"

  cat <<EOF
## 📈 角色弧光: ${name}

### 变化轨迹: ${arc_type}

---

\`\`\`
  成长
  ↑
  │          ╱ ★ 终点
  │        ╱
  │      ╱   (转折点)
  │    ╱
  │  ╱
  │╱ ★ 起点
  └─────────────────→ 故事进程
  
  ${arc_type}
\`\`\`

### 📖 弧光分解

EOF

  case "$structure" in
    3acts|三幕)
      cat <<EOF
#### 第一幕: 现状 (Act 1)

**起点状态**: "${arc_type%%→*}" 的 ${name}
- 信念: "TA相信..." (但这是错的)
- 缺陷: (阻碍TA的东西)
- 舒适区: (TA习惯的生存模式)
- 催化事件: (打破现状的事件)

#### 第二幕: 挣扎 (Act 2)

**中间状态**: 被迫面对改变
- 测试: (新环境挑战旧信念)
- 失败: (用老方法失败了)
- 中点: (TA开始意识到需要改变)
- 最低点: (失去一切，被迫选择)

#### 第三幕: 蜕变 (Act 3)

**终点状态**: "${arc_type##*→}" 的 ${name}
- 新信念: "TA现在相信..."
- 考验: (最终考验新的自我)
- 选择: (关键抉择体现变化)
- 证明: (行动证明变化是真实的)
EOF
      ;;
    *)
      cat <<EOF
#### 弧光阶段

1. **起点**: ${arc_type%%→*}
2. **催化剂**: 触发变化的事件
3. **抗拒**: 不愿意改变
4. **尝试**: 开始改变但不稳定
5. **挫折**: 变化被考验
6. **终点**: ${arc_type##*→}
EOF
      ;;
  esac

  cat <<'EOF'

### 🎯 弧光类型

| 类型 | 说明 | 经典例子 |
|------|------|---------|
| 正向弧光 | 克服缺陷，变得更好 | 大多数主角 |
| 负向弧光 | 被压力击垮，走向黑暗 | 绝命毒师·沃尔特 |
| 平坦弧光 | 角色不变，改变世界 | 超人、美队 |
| 圆形弧光 | 回到起点但有了新理解 | 霍比特人·比尔博 |

### ✅ 检查清单

- [ ] 起点和终点形成鲜明对比
- [ ] 变化是渐进的，不是突然的
- [ ] 有具体的事件触发每次变化
- [ ] 变化是可信的（不是强行洗白）
- [ ] 结局用行动（而非台词）证明变化
EOF
}


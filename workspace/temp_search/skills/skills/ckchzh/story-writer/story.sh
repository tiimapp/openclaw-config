#!/usr/bin/env bash
# story-writer — 小说创作助手
# Usage: bash story.sh <command> [text...]
# Powered by BytesAgain

set -euo pipefail

CMD="${1:-help}"
shift 2>/dev/null || true
INPUT="$*"

case "$CMD" in
  write)
    cat <<'PROMPT'
## 故事创作

请根据以下设定创作一个完整的短篇故事：

### 创作要求
1. **结构完整**：有清晰的开头、发展、高潮、结局
2. **三幕式**：
   - 第一幕（建置）：介绍角色、场景，引出核心冲突
   - 第二幕（对抗）：矛盾升级，主角遭遇困难
   - 第三幕（解决）：高潮对决，冲突解决
3. **角色鲜活**：角色有独特的性格、说话方式和行为逻辑
4. **感官描写**：调动视觉、听觉、嗅觉、触觉等感官
5. **Show, Don't Tell**：通过行动和对话展示，而非直接描述
6. **篇幅**：2000-3000字

### 故事设定/题材
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    echo "### 输出格式"
    echo "1. **故事标题**"
    echo "2. **正文**：完整故事（分段落）"
    echo "3. **创作手记**：简述创作思路和主题"
    ;;

  character)
    cat <<'PROMPT'
## 角色设计

请根据以下描述设计一个完整的故事角色：

### 角色设计维度
1. **基本信息**：
   - 姓名（含含义/由来）
   - 年龄、性别、外貌特征
   - 职业、社会地位
2. **性格特征**：
   - 核心性格（3个关键词）
   - 优点和缺点
   - MBTI 或九型人格参考
3. **背景故事**：
   - 家庭背景
   - 成长经历
   - 转折事件（塑造性格的关键事件）
4. **动机与恐惧**：
   - 表面目标（想要什么）
   - 深层需求（真正需要什么）
   - 核心恐惧（最害怕什么）
5. **行为模式**：
   - 说话风格（附示例台词3句）
   - 习惯性动作
   - 在压力下的反应
6. **人物关系**：
   - 与其他角色的关系网
   - 盟友和对手
7. **角色弧光**：
   - 故事开始时的状态
   - 经历变化
   - 故事结束时的成长

### 角色描述/要求
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    echo "### 输出格式"
    echo "按上述维度输出完整角色设计卡，每个维度都要填充"
    ;;

  plot)
    cat <<'PROMPT'
## 情节设计

请根据以下题材/要求设计故事情节结构：

### 三幕式情节设计

#### 第一幕：建置（约占25%）
- **开场钩子**：第一个吸引读者的场景
- **日常世界**：主角的日常生活
- **触发事件**：打破日常的事件
- **第一转折点**：主角被迫踏上旅程

#### 第二幕：对抗（约占50%）
- **上升行动**：困难不断升级
- **盟友与敌人**：新角色登场
- **中点转折**：重大发现或逆转
- **危机加深**：最黑暗的时刻
- **第二转折点**：找到解决问题的关键

#### 第三幕：解决（约占25%）
- **高潮准备**：最终对决前的准备
- **高潮**：核心冲突的最终对决
- **结局**：新的平衡状态
- **尾声**：暗示未来

### 关键情节点
- **核心冲突**：整个故事的主要矛盾
- **B故事线**：副线（通常是情感线）
- **主题**：故事想表达什么

### 题材/要求
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    echo "### 输出格式"
    echo "1. **一句话概括**：用一句话描述故事核心"
    echo "2. **三幕详细大纲**：每幕的关键场景和情节点"
    echo "3. **章节规划**：建议的章节划分和每章概要"
    echo "4. **冲突升级图**：冲突如何逐步升级到高潮"
    ;;

  dialogue)
    cat <<'PROMPT'
## 对话生成

请根据以下场景和角色生成自然的对话：

### 对话写作要求
1. **角色区分**：每个角色的说话方式要有辨识度
   - 用词习惯不同
   - 句式长短不同
   - 口头禅不同
2. **潜台词**：对话要有言外之意，不能太直白
3. **推动情节**：每段对话都要推动故事发展或揭示信息
4. **节奏控制**：
   - 紧张时：短句、打断、省略号
   - 轻松时：长句、调侃、感叹
5. **动作描写**：穿插角色说话时的表情、动作、心理
6. **冲突感**：对话中要有观点碰撞或情感张力

### 场景/角色设定
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    echo "### 输出格式"
    echo "1. **场景描述**：简短的环境和氛围描写"
    echo "2. **对话正文**：含动作描写和心理活动的完整对话"
    echo "3. **对话分析**：标注潜台词和冲突点"
    ;;

  worldbuild)
    cat <<'PROMPT'
## 世界观构建

请根据以下设定构建一个完整的故事世界观：

### 世界观设计维度

#### 一、基础设定
- **时间**：什么时代/纪元
- **空间**：地理环境、重要地点
- **科技/魔法水平**：技术或超自然力量体系

#### 二、社会结构
- **政治体制**：权力结构、统治方式
- **经济体系**：货币、贸易、产业
- **社会阶层**：阶级划分、流动性
- **文化习俗**：节日、禁忌、礼仪

#### 三、历史与传说
- **创世/起源**：世界的诞生
- **重大历史事件**：影响世界走向的事件
- **传说与神话**：民间流传的故事

#### 四、势力与阵营
- **主要阵营**：各方势力及其目标
- **冲突关系**：势力间的矛盾和联盟
- **关键人物**：各阵营的代表人物

#### 五、独特元素
- **特有种族/物种**
- **独特资源/能源**
- **规则/定律**：这个世界的特殊规则

### 世界设定/方向
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    echo "### 输出格式"
    echo "按上述维度输出完整世界观设定文档，每个维度至少3-5条设定"
    echo "附：**世界观一页纸**（一段话概括核心设定，方便快速参考）"
    ;;

  continue)
    cat <<'PROMPT'
## 故事续写

请根据以下已有内容续写故事：

### 续写要求
1. **风格一致**：保持与原文一致的文风、叙事视角和语气
2. **逻辑连贯**：续写内容与已有情节保持逻辑一致
3. **角色一致**：角色的性格、说话方式保持不变
4. **情节推进**：续写要推动故事发展，不能原地踏步
5. **悬念设置**：在适当位置设置悬念或钩子
6. **篇幅**：续写约 1000-2000 字

### 续写策略
- 分析已有文本的叙事节奏和风格
- 找出待解决的悬念和伏笔
- 确定下一个情节转折点
- 保持人物弧光的发展方向

### 已有故事内容
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    echo "### 输出格式"
    echo "1. **续写分析**：简述对已有内容的理解和续写方向"
    echo "2. **续写正文**：完整的续写内容"
    echo "3. **后续建议**：接下来故事可能的发展方向（2-3个选项）"
    ;;

  help|--help|-h|"")
    cat <<'HELP'
╔══════════════════════════════════════════════╗
║        ✍️ Story Writer — 小说创作助手         ║
╠══════════════════════════════════════════════╣
║                                              ║
║  Commands:                                   ║
║    write      创作完整短篇故事                ║
║    character  设计角色                        ║
║    plot       设计情节（三幕式）              ║
║    dialogue   生成角色对话                    ║
║    worldbuild 构建世界观                      ║
║    continue   续写已有故事                    ║
║    help       显示此帮助菜单                  ║
║                                              ║
║  Usage:                                      ║
║    bash story.sh <command> [description]      ║
║                                              ║
║  Examples:                                   ║
║    bash story.sh write 赛博朋克黑客故事       ║
║    bash story.sh character 退休刑警           ║
║    bash story.sh plot 密室杀人案              ║
║    bash story.sh dialogue 老友酒吧重逢        ║
║    bash story.sh worldbuild 火星殖民地        ║
║                                              ║
╚══════════════════════════════════════════════╝
  Powered by BytesAgain | bytesagain.com | hello@bytesagain.com
HELP
    ;;

  *)
    echo "❌ Unknown command: $CMD"
    echo "Run 'bash story.sh help' for usage."
    exit 1
    ;;
esac
